UPDATE EC_APP.APP_USERS
   SET EMAIL_VERIFIED = CASE WHEN NVL(IS_VERIFIED, 'N') = 'Y' THEN 'Y' ELSE 'N' END
 WHERE NVL(EMAIL_VERIFIED, 'N') <> CASE WHEN NVL(IS_VERIFIED, 'N') = 'Y' THEN 'Y' ELSE 'N' END;

UPDATE EC_APP.APP_USERS
   SET VERIFIED_AT = NVL(VERIFIED_AT, CAST(CREATED_AT AS TIMESTAMP))
 WHERE EMAIL_VERIFIED = 'Y'
   AND VERIFIED_AT IS NULL;

COMMIT;

