PROMPT [1/4] Creating AUTH_REFRESH_TOKENS table if needed...
DECLARE
  v_count NUMBER := 0;
BEGIN
  SELECT COUNT(*)
    INTO v_count
    FROM ALL_TABLES
   WHERE OWNER = 'EC_APP'
     AND TABLE_NAME = 'AUTH_REFRESH_TOKENS';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE q'[
      CREATE TABLE EC_APP.AUTH_REFRESH_TOKENS (
        ID            NUMBER GENERATED BY DEFAULT AS IDENTITY,
        USER_ID       NUMBER NOT NULL,
        TOKEN_HASH    VARCHAR2(64 CHAR) NOT NULL,
        EXPIRES_AT    TIMESTAMP NOT NULL,
        USER_AGENT    VARCHAR2(512 CHAR) NULL,
        IP_ADDRESS    VARCHAR2(64 CHAR) NULL,
        CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        LAST_USED_AT  TIMESTAMP NULL,
        REVOKED_AT    TIMESTAMP NULL,
        REVOKE_REASON VARCHAR2(120 CHAR) NULL,
        CONSTRAINT PK_AUTH_REFRESH_TOKENS PRIMARY KEY (ID),
        CONSTRAINT FK_ART_USER FOREIGN KEY (USER_ID) REFERENCES EC_APP.APP_USERS (ID),
        CONSTRAINT CK_ART_TOKEN_HASH_LEN CHECK (LENGTH(TOKEN_HASH) = 64)
      )
    ]';
  END IF;
END;
